namespace Domain.Entities;

// Типы шаблонов (паттернов) для графических объектов или фоновой заливки
public enum PatternType
{
    Tiles,      // Плиточный паттерн: повторяющиеся квадраты или прямоугольники
    Circles,    // Круги: повторяющиеся или распределённые окружности
    Gradient,   // Градиент: плавное изменение цвета по направлению
    Stripes     // Полосы: параллельные линии или полосатый узор
}

